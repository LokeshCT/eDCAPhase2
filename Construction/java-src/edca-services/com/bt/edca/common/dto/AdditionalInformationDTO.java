package com.bt.edca.common.dto;


/**
 * class for setting and getting the values.
 * @author Rohit.
 *.
 */
public class AdditionalInformationDTO {

	private String cotNotes;
	private String notes;
	private String productNotes;
	private String overrideAuthorisation;
	private String authorisationOverrideReason;
	private long orderId;
	
  /**
   * 
   * @param cotNotes
   */
  public void setCotNotes(String cotNotes) {
      this.cotNotes = cotNotes;
  }

  /**
	  * @return cotNotes -getter for cotNotes .
	 */
	public String getCotNotes() {
		return cotNotes;
	}
	/**
	 * @param notes -setter for notes.
	 */
	public void setNotes(String notes) {
		this.notes = notes;
	}
	/**
	 * @return notes -getter for notes .
	 */
	public String getNotes() {
		return notes;
	}
	/** 
	 * @return orderId -getter for orderId.
	 */
	public long getOrderId() {
		return orderId;
	}
	/**
	 * @param orderId -setter for orderId .
	 */
	public void setOrderId(long orderId) {
		this.orderId = orderId;
	}
	/**
	 * @param authorisationOverrideReason -setter for authorisationOverrideReason .
	 */
	public void setAuthorisationOverrideReason(
			String authorisationOverrideReason) {
		this.authorisationOverrideReason = authorisationOverrideReason;
	}
	/** 
	 * @return authorisationOverrideReason -getter for authorisationOverrideReason .
	 */
	public String getAuthorisationOverrideReason() {
		return authorisationOverrideReason;
	}
	/**
	 * @param overrideAuthorisation -setter for overrideAuthorisation .
	 */
	public void setOverrideAuthorisation(String overrideAuthorisation) {
		this.overrideAuthorisation = overrideAuthorisation;
	}
	/**
	 * @return overrideAuthorisation - getter for overrideAuthorisation .
	 */
	public String getOverrideAuthorisation() {
		return overrideAuthorisation;
	}
	/** 
	 * @param productNotes -setter for productNotes .
	 */
	public void setProductNotes(String productNotes) {
		this.productNotes = productNotes;
	}
	/** 
	 * @return productNotes - getter for productNotes .
	 */
	public String getProductNotes() {
		return productNotes;
		}
}
