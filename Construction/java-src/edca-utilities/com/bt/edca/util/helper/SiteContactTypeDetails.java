
package com.bt.edca.util.helper;

/**
 * WrapperClass for twolists.
 * 
 * @author Jagankumar G.
 *
 */
public enum SiteContactTypeDetails {

  PRIMARYCONTACTTYPE("P"), SECONDARYCONTACTYPE("S");

  /**
   * declaration of contactType.
   */
  private String contactType;

  private SiteContactTypeDetails(String contactType) {
    this.contactType = contactType;
  }

  /**
   * Gets the contactType for enum.
   * 
   * @return contactType.
   */
  public String getContactType() {
    return contactType;

  }
  
  public void setContactType(String contactType) {
    this.contactType = contactType;
    }
  
  @Override
  public String toString() {
      return contactType;
  }


}
